package com.votrenom.bataillenavale.model; // Adaptez le nom du package

public class Coordonnee {
    private final int x;
    private final int y;

    public Coordonnee(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
